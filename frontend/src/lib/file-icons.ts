export function getFileIcon(name: string): string {
  const lower = name.toLowerCase();
  if (filenameMap[lower]) return filenameMap[lower];
  const dot = lower.lastIndexOf(".");
  if (dot === -1) return icon("default-file");
  const ext = lower.slice(dot);
  return extensionMap[ext] ?? icon("default-file");
}

export function getFolderIcon(name: string, open: boolean): string {
  const lower = name.toLowerCase();
  const base = folderMap[lower];
  if (base) {
    return open ? base + "-opened" : base;
  }
  return open ? icon("default-folder-opened") : icon("default-folder");
}
